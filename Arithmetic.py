number_one = 27.5 
number_two = 2

addition = number_one + number_two 
print(number_one, "+", number_two, "=", addition)

subtraction = number_one - number_two 
print(number_one, "-", number_two, "=", subtraction)

multiplication = number_one * number_two 
print(number_one, "*", number_two, "=", multiplication)

division = number_one / number_two 
print(number_one, "/", number_two, "=", division)

floor_value =  number_one // number_two 
print(number_one, "//", number_two, "=", floor_value)

exponential = number_one ** number_two 
print(number_one, "**", number_two, "=", exponential)

